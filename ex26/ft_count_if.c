int	ft_count_if(char **tab, int (*f)(char *))
{
	int	cpt;
	int	index;

	cpt = 0;
	index = 0;
	while (tab && tab[index])
	{
		if (f(tab[index++]) == 1)
			cpt++;
	}
	return (cpt);
}
