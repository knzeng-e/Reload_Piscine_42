int	ft_strcmp(char *s1, char *s2)
{
	char	*str1;
	char	*str2;

	if (!s1 && s2)
		return (-1);
	if (s1 && !s2)
		return (1);
	str1 = s1;
	str2 = s2;
	while (*str1 && *str2)
	{
		if (*str1 != *str2)
			return (*str1 - *str2);
		str1++;
		str2++;
	}
	return (*str1 - *str2);
}
